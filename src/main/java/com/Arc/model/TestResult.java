package com.Arc.model;

public class TestResult {
Integer correctAns;
Integer wrongAns;
Integer noAns;
Integer outof;
Integer totalMarks;
String testResult;
public Integer getCorrectAns() {
	return correctAns;
}
public void setCorrectAns(Integer correctAns) {
	this.correctAns = correctAns;
}
public Integer getWrongAns() {
	return wrongAns;
}
public void setWrongAns(Integer wrongAns) {
	this.wrongAns = wrongAns;
}
public Integer getNoAns() {
	return noAns;
}
public void setNoAns(Integer noAns) {
	this.noAns = noAns;
}
public Integer getOutof() {
	return outof;
}
public void setOutof(Integer outof) {
	this.outof = outof;
}
public Integer getTotalMarks() {
	return totalMarks;
}
public void setTotalMarks(Integer totalMarks) {
	this.totalMarks = totalMarks;
}
public String getTestResult() {
	return testResult;
}
public void setTestResult(String testResult) {
	this.testResult = testResult;
}

}
